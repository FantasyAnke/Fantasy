package com.lzp.moviedb.entity;

public class Moviehall {
	private Integer mhId;
	private String mhName;
	private String mhDesc;
	private Integer movieId;
	private Integer filmId;
	
	
	public Integer getMhId() {
		return mhId;
	}
	public void setMhId(Integer mhId) {
		this.mhId = mhId;
	}
	public String getMhName() {
		return mhName;
	}
	public void setMhName(String mhName) {
		this.mhName = mhName;
	}
	public String getMhDesc() {
		return mhDesc;
	}
	public void setMhDesc(String mhDesc) {
		this.mhDesc = mhDesc;
	}
	public Integer getMovieId() {
		return movieId;
	}
	public void setMovieId(Integer movieId) {
		this.movieId = movieId;
	}
	public Integer getFilmId() {
		return filmId;
	}
	public void setFilmId(Integer filmId) {
		this.filmId = filmId;
	}
	
	

}
